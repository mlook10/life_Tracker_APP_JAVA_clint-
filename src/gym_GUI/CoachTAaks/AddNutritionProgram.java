    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = JOptionPane.showInputDialog(null, "Enter the name:");
        
        String ingredientsString = JOptionPane.showInputDialog(null, "Enter the ingredients separated by commas:");
        List<String> ingredients = new ArrayList<>();
        for (String ingredient : ingredientsString.split(",")) {
            ingredients.add(ingredient.trim());
        }
        String nutritionalInfo = JOptionPane.showInputDialog(null, "Enter the nutritional info:");
        Meal m=new Meal(name, ingredients, nutritionalInfo);
        meals.add(m);
        jTextArea1.append(m.toString()+"\n");
    }//GEN-LAST:event_jButton1ActionPerformed
