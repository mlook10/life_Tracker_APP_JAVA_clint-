    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        SpecializedNutritionProgram s=new SpecializedNutritionProgram();
        for (Meal meal : meals) {
            s.addMeal(meal);
        }
        ((Participant)user).setNutritionProgram(s);
        JOptionPane.showMessageDialog(null, "Nutrition Program added successfully..");
    }//GEN-LAST:event_jButton2ActionPerformed


